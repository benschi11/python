# Schreibe ein Programm, dass eine Dezimalzahl
# einliest und das doppelte der Zahl ausgibt

# ZUSATZ: Der Benutzer gibt zwei Zahlen a und b ein
# und es wird a * b ausgeben

a = float(input("Bitte geben Sie eine Zahl ein:"));
ergebnis = a * 2;
print("Das Doppelte von " + str(a) + " ist " + str(ergebnis));